public class ForLoops {

    public static void main(String[] args) {
        for (int i = 1; i <= 3; i++) {
            System.out.println(i);
        }

        for (int i = 1; i <= 10; i++) {
            System.out.println("If Java was easy, they would call it Python!");
        }

        for (int i = 1; i <=5 ; i++) {
            System.out.println("This is a for loop");
        }
    }

}
